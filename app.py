    suspicious_words = ["login", "verify", "secure", "update", "confirm", "bank", "paypal", "password", "account", "alert", "подтвердить", "войти", "безопасность"]
    suspicious_domains = ["bit.ly", "tinyurl", "goo.gl", "rb.ly", "clck.ru", "shorturl.at", "vk.cc"]
    
    link = st.text_input("Вставь подозрительную ссылку:")
    
    if link:
        link_lower = link.lower()
        found_words = [w for w in suspicious_words if w in link_lower]
        found_domains = [d for d in suspicious_domains if d in link_lower]
        
        if found_words or found_domains:
            st.error("🚨 **ВНИМАНИЕ! Это похоже на ФИШИНГ!**")
            if found_words:
                st.write(f"⚠️ Подозрительные слова: {', '.join(found_words)}")
            if found_domains:
                st.write(f"⚠️ Подозрительный домен: {', '.join(found_domains)}")
            st.info("💡 Никогда не переходи по подозрительным ссылкам!")
        else:
            st.success("✅ Ссылка выглядит безопасно!")
            st.info("💡 Даже на красивых сайтах могут быть мошенники. Проверяй адресную строку!")

